f_smaller = 3.141592653589279
f_bigger = 3.87756392769

import math

print(math.ceil(f_smaller), math.ceil(f_bigger)) #najmniejsza całkowita większa od argumentu
print('\n')

print(math.floor(f_smaller), math.floor(f_bigger)) #największa liczba mniejsza od argumentu
print('\n')

print(math.ceil(-f_smaller), math.ceil(-f_bigger))
print('\n')

print(math.floor(-f_smaller), math.floor(-f_bigger))
print('\n')

print(pow(2,8), pow(9,0.5)) #power, w skrócie pow - liczba, do potęgi, czy pierwiastek, ale lepiej to niżej
print('\n')

print(math.sqrt(16)) #kwadratowy pierwiastek
print('\n')

print(math.pi, math.e) #stałe matematyczne
print('\n')

print(math.sin(math.pi/2), math.cos(math.pi/4)) #sinusy, cosinusy, tangensy etc.
print('\n')
